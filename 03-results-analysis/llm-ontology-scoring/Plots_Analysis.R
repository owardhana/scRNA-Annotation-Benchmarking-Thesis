# Computes the Nemenyi critical difference (CD) for k tools over N blocks at significance alpha
compute_cd <- function(k, N, alpha = 0.05) {
  q_alpha <- qtukey(1 - alpha, k, Inf) / sqrt(2)
  q_alpha * sqrt(k * (k + 1) / (6 * N))
}

# Groups tools into cliques of non-significantly-different pairs based on mean rank
build_cd_cliques <- function(mean_ranks_df, CD) {
  tools_ordered <- mean_ranks_df$tool
  ranks_ordered <- mean_ranks_df$mean_rank
  n <- length(tools_ordered)
  cliques <- list()
  
  i <- 1
  while (i <= n) {
    j <- i
    while (j < n && (ranks_ordered[j + 1] - ranks_ordered[i]) <= CD) j <- j + 1
    if (j > i) {
      cliques[[length(cliques) + 1]] <- list(
        start = ranks_ordered[i], end = ranks_ordered[j],
        from  = tools_ordered[i], to  = tools_ordered[j]
      )
      i <- j + 1
    } else {
      i <- i + 1
    }
  }
  cliques
}

# Scatter of mean score vs a dataset characteristic, pooled across all tools
make_scatter_effect <- function(data, x_col, xlabel, line_col, y_col = "score_mean", ylabel = "Mean Score", log_y = FALSE) {
  df_sub <- data %>% filter(!is.na(.data[[y_col]]))
  if (log_y) df_sub <- df_sub %>% filter(as.numeric(.data[[y_col]]) > 0)
  if (nrow(df_sub) < 3) return(ggplot() + theme_void() + labs(title = paste("Insufficient data for", ylabel)))

  plot_df <- df_sub %>%
    group_by(scenario) %>%
    summarise(
      metric_val  = mean(as.numeric(.data[[y_col]]), na.rm = TRUE),
      x_val       = mean(as.numeric(.data[[x_col]]), na.rm = TRUE),
      short_label = unique(short_label)[[1]],
      .groups     = "drop"
    )

  if (nrow(plot_df) < 3) return(ggplot() + theme_void() + labs(title = paste("Insufficient groups for", ylabel)))

  plot_df$y_vals <- if (log_y) log10(plot_df$metric_val) else plot_df$metric_val

  sp  <- cor.test(plot_df$x_val, plot_df$y_vals, method = "spearman", exact = FALSE)
  pe  <- cor.test(plot_df$x_val, plot_df$y_vals, method = "pearson")

  sig_sp <- dplyr::case_when(sp$p.value < 0.001 ~ "***", sp$p.value < 0.01 ~ "**", sp$p.value < 0.05 ~ "*", TRUE ~ "ns")
  sig_pe <- dplyr::case_when(pe$p.value < 0.001 ~ "***", pe$p.value < 0.01 ~ "**", pe$p.value < 0.05 ~ "*", TRUE ~ "ns")

  fit <- lm(y_vals ~ x_val, data = plot_df)
  cf  <- coef(fit)
  intercept <- cf[1]; slope <- cf[2]
  r_squared <- summary(fit)$r.squared
  sign_slope <- ifelse(slope < 0, "-", "+")

  y_str    <- if (log_y) "log\u2081\u2080(y)" else "y"
  eq_label <- sprintf("%s = %.3f %s %.3fx  (R\u00b2 = %.2f)", y_str, intercept, sign_slope, abs(slope), r_squared)
  sub_label <- sprintf("Spearman \u03c1 = %.2f (p = %.3f %s)  |  Pearson r = %.2f (p = %.3f %s)\nFit: %s",
                       sp$estimate, sp$p.value, sig_sp, pe$estimate, pe$p.value, sig_pe, eq_label)

  p <- ggplot(plot_df, aes(x = x_val, y = metric_val)) +
    geom_smooth(method = "lm", se = TRUE, colour = line_col, fill = line_col, alpha = 0.15, linewidth = 0.8) +
    geom_point(size = 4, colour = line_col) +
    ggrepel::geom_text_repel(aes(label = short_label), size = 2.8, colour = "grey30",
                             segment.colour = "grey60", segment.size = 0.3, max.overlaps = 20) +
    labs(x = xlabel, y = ylabel, subtitle = sub_label) +
    theme_bench() + theme(panel.grid.major.x = element_blank())

  if (log_y) p <- p + scale_y_log10(labels = label_number(big.mark = ",")) else p <- p + score_axis
  p
}

# Custom File Loader
load_llm_data <- function(data_dir) {
  tsv_files <- list.files(data_dir, pattern = "\\.tsv$", full.names = TRUE)
  if (length(tsv_files) == 0) stop("No TSV files found in: ", data_dir)
  
  all_dfs <- lapply(tsv_files, function(f) {
    fname <- basename(f)
    # Extracts dataset name and tool name from '{scenario}_subset_{tool}_cluster_map.tsv'
    pattern <- "^(.*)_subset_(.*)_cluster_map\\.tsv$"
    matches <- regmatches(fname, regexec(pattern, fname))
    
    if (length(matches[[1]]) == 3) {
      tryCatch({
        df <- read.delim(f, sep = "\t", stringsAsFactors = FALSE)
        score_cols <- grep("^score(_[0-9]+)?$", colnames(df), value = TRUE)
        if (length(score_cols) > 0) {
          tibble::tibble(
            scenario   = matches[[1]][2],
            tool       = matches[[1]][3],
            score_mean = mean(as.matrix(df[, score_cols, drop = FALSE]), na.rm = TRUE)
          )
        } else { NULL }
      }, error = function(e) { NULL })
    } else { NULL }
  })
  
  combined <- dplyr::bind_rows(Filter(Negate(is.null), all_dfs))
  message("Successfully loaded ", nrow(combined), " tool-dataset mappings.")
  combined
}

