# --- C. Friedman Test & Nemenyi Post-hoc ---
score_wide <- df_scenario_mean %>%
  dplyr::select(scenario, tool, score_mean) %>%
  pivot_wider(names_from = tool, values_from = score_mean)

score_mat_raw <- as.matrix(score_wide[, -1])
rownames(score_mat_raw) <- score_wide$scenario

# Subsetting matrices to complete cases (Drops datasets that miss a tool)
complete_scenarios <- rownames(score_mat_raw)[rowSums(is.na(score_mat_raw)) == 0]
score_mat <- score_mat_raw[complete_scenarios, , drop = FALSE]

k_tools     <- ncol(score_mat)
N_scenarios <- nrow(score_mat)

# Verify minimum threshold for Friedman
if (N_scenarios >= 2 && k_tools >= 2) {
  friedman_res <- friedman.test(score_mat)
  
  rank_mat   <- t(apply(score_mat, 1, function(row) rank(-row, ties.method = "average")))
  mean_ranks <- colMeans(rank_mat, na.rm = TRUE)
  mean_ranks_df <- tibble::tibble(tool = names(mean_ranks), mean_rank = mean_ranks) %>% arrange(mean_rank)
  
  CD      <- compute_cd(k_tools, N_scenarios)
  cliques <- build_cd_cliques(mean_ranks_df, CD)
  
  if (friedman_res$p.value < 0.05) {
    nemenyi_res <- PMCMRplus::frdAllPairsNemenyiTest(y = score_mat, p.adjust.method = "none")
    p_mat       <- nemenyi_res$p.value
  } else { p_mat <- NULL }
  
} else {
  friedman_res  <- list(p.value = 1, statistic = NA, parameter = NA)
  mean_ranks_df <- tibble::tibble(tool = colnames(score_mat_raw), mean_rank = NA)
  cliques       <- list()
  p_mat         <- NULL
  CD            <- NA
}

