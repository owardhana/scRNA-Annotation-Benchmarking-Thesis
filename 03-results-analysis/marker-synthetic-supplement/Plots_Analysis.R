  eta_sq  = anova_res_rt[c("log_cells","n_types","shannon","knn_purity"), "Sum Sq"] / ss_total_rt,
  p_value = anova_res_rt[c("log_cells","n_types","shannon","knn_purity"), "Pr(>F)"]
