# ============================================================
# A. ETA-SQUARED: continuous proxies  vs  categorical set factors
# ============================================================
# Type III (marginal-after-all-others) SS partition throughout.
# For the categorical L9 decomposition we set sum-to-zero contrasts locally so
# that Type III SS for the balanced L9 main-effects design corresponds to the
# textbook orthogonal partition (under sum contrasts and a balanced design,
# Type III SS coincides with the standard ANOVA orthogonal partition).
# For the continuous proxy model the contrast choice is immaterial (no factors).
.saved_contrasts <- options(contrasts = c("contr.sum", "contr.poly"))

# A1. Categorical (orthogonal) decomposition on the SET L9 factors.
fit_cat <- aov(kappa_mean ~ cell_count + imbalance + n_types_set + de_signal, data = df_orth)
av_cat  <- car::Anova(fit_cat, type = 3)
tot_cat <- sum(anova(fit_cat)[["Sum Sq"]])
cat_terms <- c("cell_count", "imbalance", "n_types_set", "de_signal")
eta_cat <- tibble::tibble(
  factor = c("Cell Count", "Imbalance", "No. Cell Types", "DE Difficulty"),
  eta_sq = av_cat[cat_terms, "Sum Sq"] / tot_cat,
  p_value = av_cat[cat_terms, "Pr(>F)"],
  model  = "Categorical (set L9 levels)"
)

# A2. Continuous proxy decomposition (mirrors Plots_Analysis.R exactly).
fit_con <- lm(kappa_mean ~ log_cells + n_types + shannon + knn_purity, data = df_orth)
av_con  <- car::Anova(fit_con, type = 3)
tot_con <- sum(anova(fit_con)[["Sum Sq"]])
con_terms <- c("log_cells", "n_types", "shannon", "knn_purity")
eta_con <- tibble::tibble(
  factor = c("Cell Count", "No. Cell Types", "Imbalance", "DE Difficulty"),  # proxy -> design dim
  eta_sq = av_con[con_terms, "Sum Sq"] / tot_con,
  p_value = av_con[con_terms, "Pr(>F)"],
  model  = "Continuous (profiling proxies)"
)

eta_compare <- bind_rows(eta_cat, eta_con) %>%
  mutate(
    factor = factor(factor, levels = c("Cell Count","Imbalance","No. Cell Types","DE Difficulty")),
    sig    = case_when(p_value < 0.001 ~ "***", p_value < 0.01 ~ "**",
                       p_value < 0.05 ~ "*", TRUE ~ "ns")
  )

cat("\n=== Between-scenario variance explained ===\n")
cat(sprintf("Categorical (saturates the 9-run design): %.1f%%\n", 100 * sum(eta_cat$eta_sq)))
cat(sprintf("Continuous (linear part only):            %.1f%%\n", 100 * sum(eta_con$eta_sq)))
print(eta_compare)

p_eta <- ggplot(eta_compare, aes(x = factor, y = eta_sq, fill = model)) +
  geom_col(position = position_dodge(width = 0.75), width = 0.7) +
  geom_text(aes(label = sprintf("%.1f%%%s", 100 * eta_sq, ifelse(sig == "ns", "", sig))),
            position = position_dodge(width = 0.75), vjust = -0.4, size = 2.6) +
  scale_fill_manual(values = c("Categorical (set L9 levels)" = "#34495E",
                               "Continuous (profiling proxies)" = "#95A5A6"), name = NULL) +
  scale_y_continuous(labels = percent_format(accuracy = 1), expand = expansion(mult = c(0, 0.12))) +
  labs(title = "Variance attribution: orthogonal set factors vs continuous proxies (Synthetic, P4)",
       subtitle = "Cohen's κ. Categorical model uses the actual orthogonal L9 levels; continuous mirrors the main analysis. Concordance = robustness.",
       x = NULL, y = "η² (share of total variance)") +
  theme_bench()
ggsave("eta2_continuous_vs_categorical.png", p_eta, width = 254, height = 130,
