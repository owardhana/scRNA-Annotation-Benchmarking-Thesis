# ============================================================
# P4 Synthetic — ORTHOGONAL L9 FACTOR DECOMPOSITION
# Companion to Plots_Analysis.R (set working dir to this folder before running).
#
# Purpose (confirmatory / rigor backstop for the orthogonal-design claim):
#   The main analysis decomposes Cohen's kappa variance using CONTINUOUS
#   profiling proxies (log cells, #types, Shannon entropy, kNN purity). kNN
#   purity is a downstream MEDIATOR of the design factors, so that model is not
#   an orthogonal-design analysis. Here we re-decompose kappa on the ACTUAL set
#   L9 factors (cell count, imbalance, #types, DE difficulty) as CATEGORICAL
#   factors — which ARE orthogonal by construction — and compare the two.
#
#   Three outputs (./Plots_OrthogonalFactors):
#     1. eta2_continuous_vs_categorical.png  — concordance of the two decompositions
#     2. l9_main_effects_setfactors.png      — Taguchi main-effects (mean kappa per set level)
#     3. mediation_purity_by_setfactors.png  — how the set factors create (lack of) separability
#
#   The continuous purity->kappa relationship (the headline mechanism/predictor)
#   is UNCHANGED and lives in the main Plots_Analysis.R; it remains the
#   directly P4<->P5 comparable analysis. This script adds the P4-only orthogonal
#   layer; P5 (real data) has no orthogonal counterpart by construction.
# ============================================================

