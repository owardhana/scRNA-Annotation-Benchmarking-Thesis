# ============================================================
# C. MEDIATION BRIDGE: how the set factors drive separability (kNN purity)
# ============================================================
# Scenario-level (one purity value per scenario) so this is a clean 9-run decomposition.
df_purity <- df_orth %>% distinct(scenario, knn_purity, cell_count, imbalance, n_types_set, de_signal)
fit_med <- aov(knn_purity ~ cell_count + imbalance + n_types_set + de_signal, data = df_purity)
# This model is SATURATED by construction: 9 set-level scenarios with four 3-level
# factors consume 8 df + 1 intercept = 9, leaving residual df = 0. car::Anova(type = 3)
# refuses to fit because it cannot compute F-tests against a zero-df residual.
# Base anova() is used here purely to extract sums-of-squares for the η² partition;
# no inference is drawn (the saturated nature is stated explicitly in Methods §
# Statistical analysis and Sup Fig S3, Panel B as "descriptive rather than inferential").
# Because the L9 design is balanced and the model contains main effects only,
# Type I and Type III SS coincide mathematically — the η² values reported here are
# the Type III values for any non-saturated grain at which they would be estimable.
av_med  <- anova(fit_med); tot_med <- sum(av_med[["Sum Sq"]])

eta_med <- tibble::tibble(
  factor = c("Cell Count", "Imbalance", "No. Cell Types", "DE Difficulty"),
  eta_sq = av_med[cat_terms, "Sum Sq"] / tot_med
) %>% mutate(factor = factor(factor, levels = names(FACTOR_COLOURS)))

# Restore the user's prior contrasts now that the categorical L9 decompositions are done.
options(.saved_contrasts)

cat("\n=== Mediation: variance in kNN purity explained by set factors ===\n"); print(eta_med)

p_med <- ggplot(eta_med, aes(x = fct_rev(factor), y = eta_sq, fill = factor)) +
  geom_col(width = 0.7) +
  geom_text(aes(label = sprintf("%.1f%%", 100 * eta_sq)), hjust = -0.15, size = 2.8) +
  coord_flip(ylim = c(0, min(1, max(eta_med$eta_sq) * 1.25))) +
  scale_fill_manual(values = FACTOR_COLOURS, guide = "none") +
  scale_y_continuous(labels = percent_format(accuracy = 1)) +
  labs(title = "Mediation: which design factors create (lack of) separability?",
       subtitle = "η² of set L9 factors on kNN purity. Purity sits downstream of the knobs -> it is a mediator, not a peer predictor of κ.",
       x = NULL, y = "η² (share of purity variance)") +
  theme_bench()
ggsave("mediation_purity_by_setfactors.png", p_med, width = 200, height = 110,
       units = "mm", dpi = 600, path = PLOT_DIR)

