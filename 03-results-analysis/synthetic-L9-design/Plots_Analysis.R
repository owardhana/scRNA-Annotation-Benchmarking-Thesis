# Memory analogue of plot 34: which dataset factors explain the most peak-memory
# variance within each algorithm category? High \u03b7\u00b2 for cell count suggests O(N) memory scaling.
# PLOT 38: Category-stratified factor sensitivity \u2014 Peak Memory (\u03b7\u00b2 per category)
eta_mem_by_category <- purrr::map_dfr(names(CATEGORY_COLOURS), function(cat) {
  d <- df_profiles %>% filter(tool_category == cat,
                              !is.na(peak_system_memory_mean_mb),
                              peak_system_memory_mean_mb > 0) %>%
    mutate(log_memory = log10(peak_system_memory_mean_mb))
  if (nrow(d) < 15) return(NULL)
  tryCatch({
    fit  <- lm(log_memory ~ log_cells + n_types + shannon + knn_purity, data = d)
    anov <- car::Anova(fit, type = 3)
    ss_tot <- sum(anova(fit)[["Sum Sq"]])
    .preds <- c("log_cells", "n_types", "shannon", "knn_purity")
    tibble::tibble(
      category = cat,
      factor   = .preds,
      eta_sq   = anov[.preds, "Sum Sq"] / ss_tot,
      p_value  = anov[.preds, "Pr(>F)"]
    )
  }, error = function(e) NULL)
}) %>% mutate(
  category = factor(category, levels = names(CATEGORY_COLOURS)),
  factor_label = recode(factor,
                        "log_cells"  = "log\u2081\u2080(Cell Count)",
                        "n_types"    = "No. Cell Types",
                        "shannon"    = "Shannon Entropy (Imbalance)",
                        "knn_purity" = "KNN Purity (Difficulty)"
  ),
  sig_stars = case_when(p_value < 0.001 ~ "***", p_value < 0.01 ~ "**", p_value < 0.05 ~ "*", TRUE ~ "ns")
)

