# Drop the unnamed or 'X' index column that read.csv sometimes adds.
.drop_index_col <- function(df) {
  index_names <- c("", "X", "Unnamed: 0")
  if (ncol(df) > 0 && colnames(df)[1] %in% index_names) {
    df <- df[, -1, drop = FALSE]
  }
  df
}

