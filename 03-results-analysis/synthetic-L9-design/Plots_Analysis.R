sc_I33 <- make_scatter_effect_rt(df_profiles, "shannon",    "Shannon Entropy (Imbalance)",  "#E74C3C")
sc_K33 <- make_scatter_effect_rt(df_profiles, "n_types",    "No. Cell Types",               "#2ECC71")
