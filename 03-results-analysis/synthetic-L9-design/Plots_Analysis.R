rds_files <- sort(list.files(SEURAT_DIR, pattern = "^S[0-9]+_rep[0-9]+_seurat_subset\\.rds$",
                             full.names = TRUE, recursive = FALSE))

if (length(rds_files) == 0) {
  message("No .rds files found in SEURAT_DIR: ", SEURAT_DIR, " — skipping UMAP section.")
} else {
  message("Found ", length(rds_files), " .rds file(s). Generating UMAPs...")
  
  for (rds_path in rds_files) {
    dataset_name <- tools::file_path_sans_ext(basename(rds_path))
    
    tryCatch({
      message("  Processing: ", dataset_name)
      seurat_obj <- readRDS(rds_path)
      
      if (!"Ground_Truth_Celltype" %in% colnames(seurat_obj@meta.data)) {
        warning("  No 'Ground_Truth_Celltype' column in ", dataset_name, " — skipping.")
        next
      }
      
      Idents(seurat_obj) <- "Ground_Truth_Celltype"
      n_pcs <- min(30L, ncol(Embeddings(seurat_obj, "pca")))

      seurat_obj <- RunUMAP(seurat_obj, dims = seq_len(n_pcs))

      scenario_id    <- sub("_rep.*", "", dataset_name)
      scenario_label <- SCENARIO_LABELS[scenario_id]
      plot_title     <- if (!is.na(scenario_label)) scenario_label else dataset_name

      umap_truth <- DimPlot(seurat_obj, reduction = "umap", group.by = "Ground_Truth_Celltype", label = FALSE) +
        ggtitle(paste0(plot_title, "\nGround Truth Cell Types"))

      plot(umap_truth)

      out_file <- paste0(dataset_name, "_UMAP.png")
      ggsave(out_file, plot = umap_truth, width = 178, height = 152, units = "mm", dpi = 1200, path = PLOT_DIR)
      message("  Saved: ", file.path(PLOT_DIR, out_file))
      
    }, error = function(e) {
      warning("  Failed to process ", dataset_name, ": ", e$message)
    })
  }
}
