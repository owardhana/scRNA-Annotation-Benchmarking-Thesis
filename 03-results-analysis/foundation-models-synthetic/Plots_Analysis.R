    geom_point(size = 4, colour = line_col) +
    ggrepel::geom_text_repel(aes(label = short_label), size = 2.8, colour = "grey30",
                             segment.colour = "grey60", segment.size = 0.3, max.overlaps = 20) +
    scale_y_log10(labels = label_number(suffix = "s", big.mark = ",")) +
    labs(x = xlabel, y = "Mean runtime (s, log scale)", subtitle = sub_label) +
    theme_bench() + theme(panel.grid.major.x = element_blank())
}

# Loader: P8a CSV is a single flat file with one row per (model, scenario, replicate).
# Renames metric columns to the canonical *_mean names used downstream so the
# rest of the pipeline (mirrors P6a) needs no modification.
load_fm_flat <- function(csv_path) {
  raw <- read.csv(csv_path, stringsAsFactors = FALSE, check.names = FALSE)
  cat_map <- .build_category_map()
  combined <- raw %>%
    dplyr::rename(
      tool                       = model,
      kappa_mean                 = kappa,
      f1_mean                    = macro_f1,
      mcc_mean                   = mcc,
      rare_f1_mean               = rare_type_f1,
      runtime_mean               = runtime_s,
      peak_system_memory_mean_mb = peak_gpu_mb
    ) %>%
    mutate(tool_category = unname(cat_map[tool]))
  front <- c("scenario", "replicate", "tool", "tool_category")
  combined <- combined[, c(front, setdiff(colnames(combined), front))]
  message("Loaded ", nrow(combined), " rows | ",
          length(unique(combined$scenario)), " scenario(s) | ",
          length(unique(combined$tool)), " model(s)")
  combined
}


